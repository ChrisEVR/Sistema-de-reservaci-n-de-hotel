    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if(txtidhabitacion.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una habitacion","Error",JOptionPane.ERROR_MESSAGE);
            txtidhabitacion.requestFocus();
            return;
        }
        if(txtidcliente.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes seleccionar un cliente","Error",JOptionPane.ERROR_MESSAGE);
            txtidcliente.requestFocus();
            return;
        }
        
        
        Vreserva dts = new Vreserva();
        Freserva func = new Freserva();
        
        dts.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
        dts.setIdcliente(Integer.parseInt(txtidcliente.getText()));
        dts.setIdtrabajador(idusuario);
        
        int seleccionado=cbotipo_reserva.getSelectedIndex();
        dts.setTipo_reserva((String)cbotipo_reserva.getItemAt(seleccionado));
        
        Calendar cal,cal1,cal2;
        int d,m,a,d1,m1,a1,d2,m2,a2;
        cal = dcfecha_reserva.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR)-1900;
        dts.setFecha_reserva(new Date(a,m,d));
        
        cal1 = dcfecha_ingresa.getCalendar();
        d1=cal1.get(Calendar.DAY_OF_MONTH);
        m1=cal1.get(Calendar.MONTH);
        a1=cal1.get(Calendar.YEAR)-1900;
        dts.setFecha_ingresa(new Date(a1,m1,d1));
        
        cal2 = dcfecha_salida.getCalendar();
        d2=cal2.get(Calendar.DAY_OF_MONTH);
        m2=cal2.get(Calendar.MONTH);
        a2=cal2.get(Calendar.YEAR)-1900;
        dts.setFecha_salida(new Date(a2,m2,d2));
        
        Date fechaFin=new Date(a2,m2,d2);
        Date fechaIn=new Date(a1,m1,d1);
        
        int dias = (int) (fechaFin.getTime()-fechaIn.getTime())/86400000;
        System.out.println(dias);
        
        Double total=dias*Double.parseDouble(txtdia.getText());
        dts.setCosto_alojamiento(total);
        
        seleccionado=cboestado.getSelectedIndex();
        dts.setEstado((String)cboestado.getItemAt(seleccionado));
        
        
        
        
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(null, "La reserva fue registrada satisfactoriamente","Reserva registrada",JOptionPane.INFORMATION_MESSAGE);
                mostrar("");
                inhabilitar();
                
                //Ocupamos la habitaci√≥n alquilada
                Fhabitacion func3=new Fhabitacion();
                Vhabitacion dts3 = new Vhabitacion();
                
                dts3.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                func3.ocupar(dts3);
                func3.mostrar("");
            }
            
            
        }else if(accion.equals("editar")){
            dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
            dts.setIdtrabajador(Integer.parseInt(txtidtrabajador.getText()));
            if(func.editar(dts)){
                JOptionPane.showMessageDialog(null, "La reserva fue modificada satisfactoriamente","Reserva modificada",JOptionPane.INFORMATION_MESSAGE);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidreserva.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¬øEstas seguro de eliminar esta reserva?","Advertencia",2,JOptionPane.WARNING_MESSAGE);
            
            if (confirmacion==0) {
                Freserva func = new Freserva();
                Vreserva dts = new Vreserva();
                
                dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
                Fhabitacion func3=new Fhabitacion();
                Vhabitacion dts3 = new Vhabitacion();
                dts3.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                func3.desocupar(dts3);
                frmhabitacion form = new frmhabitacion();
                form.mostrar("");
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed


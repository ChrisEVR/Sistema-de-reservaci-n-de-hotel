    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if(txtidproducto.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes seleccionar un producto","Error",JOptionPane.ERROR_MESSAGE);
            btnbuscarproducto.requestFocus();
            return;
        }
        if(txtcantidad.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes ingresar una cantidad de producto","Error",JOptionPane.ERROR_MESSAGE);
            txtcantidad.requestFocus();
            return;
        }
        if(txtprecio_venta.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes ingresar un precio de venta de producto","Error",JOptionPane.ERROR_MESSAGE);
            txtprecio_venta.requestFocus();
            return;
        }

        Vconsumo dts = new Vconsumo();
        Fconsumo func = new Fconsumo();

        dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
        dts.setIdproducto(Integer.parseInt(txtidproducto.getText()));
        dts.setCantidad(Double.parseDouble(txtcantidad.getText()));
        dts.setPrecio_venta(Double.parseDouble(txtprecio_venta.getText()));

        int seleccionado=cboestado.getSelectedIndex();
        dts.setEstado((String)cboestado.getItemAt(seleccionado));

        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(null, "El consumo "+ txtproducto.getText() +" del cliente "+txtcliente.getText()+" fue registrado correctamente","Consumo registrado",JOptionPane.INFORMATION_MESSAGE);
                mostrar(idreserva);
                inhabilitar();
            }

        }else if(accion.equals("editar")){
            dts.setIdconsumo(Integer.parseInt(txtidconsumo.getText()));

            if(func.editar(dts)){
                JOptionPane.showMessageDialog(null, "El consumo del cliente "+txtcliente.getText()+" fue editado correctamente","Consumo modificado",JOptionPane.INFORMATION_MESSAGE);
                mostrar(idreserva);
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed


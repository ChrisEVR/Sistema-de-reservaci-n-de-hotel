    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if(txtnombre.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes ingresar un nombre para el cliente","Error",JOptionPane.ERROR_MESSAGE);
            txtnombre.requestFocus();
            return;
        }
        if(txtapaterno.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes ingresar un apellido descripciÃ³n para el cliente","Error",JOptionPane.ERROR_MESSAGE);
            txtapaterno.requestFocus();
            return;
        }
        if(txtamaterno.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes ingresar un apellido descripciÃ³n para el cliente","Error",JOptionPane.ERROR_MESSAGE);
            txtamaterno.requestFocus();
            return;
        }
        if(txtnum_documento.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes ingresar un numero de documento para el cliente","Error",JOptionPane.ERROR_MESSAGE);
            txtnum_documento.requestFocus();
            return;
        }
        if(txtcodigo_cliente.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Debes ingresar un codigo para el cliente","Error",JOptionPane.ERROR_MESSAGE);
            txtcodigo_cliente.requestFocus();
            return;
        }
        
        
        Vcliente dts = new Vcliente();
        Fcliente func = new Fcliente();
        
        dts.setNombre(txtnombre.getText());
        dts.setApaterno(txtapaterno.getText());
        dts.setAmaterno(txtamaterno.getText());
        
        int seleccionado=cbotipo_documento.getSelectedIndex();
        dts.setTipo_documento((String)cbotipo_documento.getItemAt(seleccionado));
        
        dts.setNum_documento(txtnum_documento.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setCodigo_cliente(txtcodigo_cliente.getText());
                
        
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(null, "El cliente fue registrado correctamente","Cliente registrado",JOptionPane.INFORMATION_MESSAGE);
                mostrar("");
                inhabilitar();
            }
            
            
        }else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));
            
            if(func.editar(dts)){
                JOptionPane.showMessageDialog(null, "El cliente fue modificado correctamente","Cliente modificado",JOptionPane.INFORMATION_MESSAGE);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidpersona.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar este cliente?","Advertencia",2,JOptionPane.WARNING_MESSAGE);
            
            if (confirmacion==0) {
                Fcliente func = new Fcliente();
                Vcliente dts = new Vcliente();
                
                dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

